    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String password=jTextField4.getText();
        int rol=UsuarioActual.getRol();
        
        
        if(rol==1){
           UsuarioActual.setPassword(password);
           ArchivoUsuario.guardarUsuarios(usuarios);
           JOptionPane.showMessageDialog(this, "Contraseña cambiada exitosamente.");
        } else if(rol >= 2){
            
        boolean tam= false;
        boolean may= false;
        boolean min= false;
        boolean num= false;
        boolean especial= false;
        if(password.length()>=6){
            tam=true;
        }
        for(char c: password.toCharArray()){
            if(Character.isUpperCase(c)){
                may=true;
            } else if(Character.isLowerCase(c)){
                min=true;
            } else if(Character.isDigit(c)){
                num=true;
            } else {
                especial=true;
            }
        }
        
        
        if(tam ==true && may==true && min==true && num==true && especial==true){
            UsuarioActual.setPassword(password);
            ArchivoUsuario.guardarUsuarios(usuarios);
            JOptionPane.showMessageDialog(this, "Contraseña cambiada exitosamente.");
        } else{
            JOptionPane.showMessageDialog(this, """
                                                Contrase\u00f1a inv\u00e1lida. Debe contener:
                                                - minimo 6 caracteres
                                                - Al menos una letra may\u00fascula
                                                - Al menos una letra min\u00fascula
                                                - Al menos un n\u00famero
                                                - Al menos un car\u00e1cter especial (@#$%^&+=!)""");
        }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

