    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Usuarios u=new Usuarios();
        
        u.setNombre(jTextField1.getText());
        u.setUsuario(jTextField2.getText());
        
        String rol=jComboBox2.getSelectedItem().toString();
        if(rol.equals("Administrador")){
            u.setRol(1);
        } else {
            u.setRol(2);
        }
        
        //u.rol = Integer.parseInt(jTextField3.getText());
        //u.password = jTextField4.getText();
        
        String password=jTextField4.getText();
        
        boolean tam= false;
        boolean may= false;
        boolean min= false;
        boolean num= false;
        boolean especial= false;
        if(password.length()>=6){
            tam=true;
        }
        for(char c: password.toCharArray()){
            if(Character.isUpperCase(c)){
                may=true;
            } else if(Character.isLowerCase(c)){
                min=true;
            } else if(Character.isDigit(c)){
                num=true;
            } else {
                especial=true;
            }
        }
        
        
        if(tam ==true && may==true && min==true && num==true && especial==true){
            u.setPassword(password);
            Proyectob.usuarios.add(u);
            usuarioxml.add(u);
            ArchivoUsuario.GuardarXML(usuarioxml);
            
            ArchivoUsuario.guardarUsuarios(usuarios);
            JOptionPane.showMessageDialog(this, "Usuario creado exitosamente.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField4.setText("");
        } else{
            JOptionPane.showMessageDialog(this, """
                                                Contrase\u00f1a inv\u00e1lida. Debe contener:
                                                - minimo 6 caracteres
                                                - Al menos una letra may\u00fascula
                                                - Al menos una letra min\u00fascula
                                                - Al menos un n\u00famero
                                                - Al menos un car\u00e1cter especial (@#$%^&+=!)""");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

